
/*

önden arttırma : ++a // önce değeri arttır sonra işlemi yap.
önden azaltma : --a  // önce değeri azalt sonra işlemi yap.
arkadan arttırma : a++ // önce işlemi yap sonra arttırma
önden azaltma : a-- // önce işlemi yap sonra azaltma
-a : elemanı negatif yapar.
+a : elemanı pozitif yapar.
!a : a ya eşit olmama durumu
 */


fun main(args: Array<String>) {

    var number=5 // değişken tanımlandı.
    println(number) // değer ekranda gösterildi.

    ++number // number değeri 1 arttırıldı.
    println(number) // değer ekranda gösterildi.

    number-- // number değeri 1 azaltıldı.
    println(number) // değer ekranda gösterildi.


}